/* =========================================================================
 * Dependencies
 * ========================================================================= */
var request = require('supertest');

/* =========================================================================
 * Constructor
 * ========================================================================= */
function Agent() {}

Agent.prototype.init = function(app) {
  var _this = this;
  
  _this.app = app;
  _this.agent = request.agent(app);
};

Agent.prototype.getAgent = function() {
  return this.agent;
};

/* =========================================================================
 * Exports
 * ========================================================================= */
module.exports = new Agent();
