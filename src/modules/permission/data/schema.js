"use strict";

/* =========================================================================
 * Dependencies
 * ========================================================================= */
var baseSchema = require('./base-schema');

/* =========================================================================
 * Schema
 * ========================================================================= */
var permissionSchema = baseSchema.extend({
  name: {
    type: String,
    trim: true,
    required: true,
    unique: true
  },
  roles: [{
    type: String,
    ref: 'Role'
  }]
});

/* =========================================================================
 * Exports
 * ========================================================================= */
module.exports = permissionSchema;
