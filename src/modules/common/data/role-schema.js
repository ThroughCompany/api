"use strict";

/* =========================================================================
 * Dependencies
 * ========================================================================= */
var baseSchema = require('./base-schema');

/* =========================================================================
 * Schema
 * ========================================================================= */
var roleSchema = baseSchema.extend({
  name: {
    type: String,
    trim: true,
    required: true,
    index: {
      unique: true
    }
  },
  permissions: [{
    type: String,
    ref: 'Permission'
  }]
});

/* =========================================================================
 * Exports
 * ========================================================================= */
module.exports = roleSchema;
